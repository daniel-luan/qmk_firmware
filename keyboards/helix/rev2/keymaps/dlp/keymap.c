void add_keylog(uint16_t keycode) {
    if ((keycode >= QK_MOD_TAP && keycode <= QK_MOD_TAP_MAX) || (keycode >= QK_LAYER_TAP && keycode <= QK_LAYER_TAP_MAX) || (keycode >= QK_MODS && keycode <= QK_MODS_MAX)) {
        keycode = keycode & 0xFF;
    } else if (keycode > 0xFF) {
        keycode = 0;
    }

    for (uint8_t i = 4; i > 0; --i) {
        keylog_str[i] = keylog_str[i - 1];
    }

    if (keycode < (sizeof(code_to_name) / sizeof(char))) {
        keylog_str[0] = pgm_read_byte(&code_to_name[keycode]);
    }
}

void render_keylogger_status(void) {
    oled_write_P(PSTR("KLGGR"), false);
    oled_write(keylog_str, false);
}

void render_layers(void) {
    oled_write_P(PSTR("Layer"), false);
    oled_write_ln_P(PSTR("Base"), IS_LAYER_ON(_QWERTY));
    // oled_write_ln_P(PSTR("Game"), IS_LAYER_ON(_GAME));
    oled_write_ln_P(PSTR("Num"), IS_LAYER_ON(_LOWER) && IS_LAYER_OFF(_ADJUST));
    oled_write_ln_P(PSTR("Sym"), IS_LAYER_ON(_RAISE) && IS_LAYER_OFF(_ADJUST));
    oled_write_ln_P(PSTR("Adj"), IS_LAYER_ON(_ADJUST));
}

void render_mod_status(uint8_t modifiers) {
    oled_write_P(PSTR("Mods:"), false);
    oled_write_P(PSTR("S"), (modifiers & MOD_MASK_SHIFT));
    oled_write_P(PSTR("C"), (modifiers & MOD_MASK_CTRL));
    oled_write_P(PSTR("A"), (modifiers & MOD_MASK_ALT));
    oled_write_ln_P(PSTR("G"), (modifiers & MOD_MASK_GUI));
}

void render_wpm(void){
    uint8_t wpm = get_current_wpm();
    char str[16];
    itoa(wpm, str, 10);
    oled_write_ln_P(PSTR("WPM:"), false);
    oled_write_ln(str, false);
}

